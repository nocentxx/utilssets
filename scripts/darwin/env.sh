export CLICOLOR=1       #打开终端配色开关
export LSCOLORS=ExGxFxdaCxDaDahbadech
#LSCOLORS=共22个字母，每个字母对应一种颜色，2个字母前景色和背景色，共11组。每一组代表一种文件类型
#11组文件类型的意思如下
# 1. directory
# 2. symbolic link
# 3. socket
# 4. pipe
# 5. executable (可执行文件，x权限)
# 6. block special
# 7. character special
# 8. executable with setuid bit set (setuid=Set User ID，属主身份)
# 9. executable without setgid bit set
# 10. directory writable to others, with sticky bit
# 11. directory writable to others, without sticky bit

#LSCOLORS中，各个字母代表的颜色如下，大小写有区别
# a 黑色
# b 红色    代表压缩文件或者压缩包
# c 绿色    代表可执行文件
# d 棕色    代表块文件
# e 蓝色    代表目录
# f 洋红色
# g 青色    代表链接
# h 浅灰色
# A 黑色粗体
# B 红色粗体
# C 绿色粗体
# D 棕色粗体
# E 蓝色粗体
# F 洋红色粗体
# G 青色粗体
# H 浅灰色粗体
# x 系统默认颜色
